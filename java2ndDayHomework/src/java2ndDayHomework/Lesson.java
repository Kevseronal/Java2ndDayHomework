package java2ndDayHomework;

public class Lesson {
	
	String lessonName;
	String instructor;
	String studentName;
	int lessonHour;
	
	public Lesson(String lessonName, String instructor, String studentName, int lessonHour) {

		this.lessonName = lessonName;
		this.instructor = instructor;
		this.studentName = studentName;
		this.lessonHour = lessonHour;
	}
	
}
